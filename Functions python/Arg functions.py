def happyBirthday(name, age): # remember that the order matters
    print("Happy Birthday to you!")
    print(f"You are now {age} years old")
    print(f"Happy Birthday, dear {name}")
    print("Happy Birthday to you!\n")

happyBirthday("Rafael", 24) # if you have changed the order in the keyword def or here at the arguments, it will print the wrong output
happyBirthday("Jonas", 22)
