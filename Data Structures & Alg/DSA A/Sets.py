# Criação de um conjunto
conjunto = {1, 2, 3, 4, 5}
print("Conjunto inicial:", conjunto)

# Adição de elementos
conjunto.add(6)
print("Conjunto após adicionar 6:", conjunto)

# Remoção de elementos
conjunto.remove(3)
print("Conjunto após remover 3:", conjunto)

# Verificação de existência
print("Existe o elemento 4?", 4 in conjunto)
